package com.example.aimss.data.model;

public class Ai {
    private Integer id;
    private String Name;
    private Long Num;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Long getNum() {
        return Num;
    }

    public void setNum(Long num) {
        Num = num;
    }

}
